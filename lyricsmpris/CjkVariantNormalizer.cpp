QChar simplifiedVariantFor(QChar character) {
    const ushort code = character.unicode();
    if (code < 0x3400) return character;

    for (const CjkVariantPair &pair : kCjkVariantPairs) {
        if (code == pair.traditional) return QChar(pair.simplified);
    }
    return character;
}

