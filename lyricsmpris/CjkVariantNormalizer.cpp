QString normalizeCjkVariants(QString value) {
    for (qsizetype index = 0; index < value.size(); ++index) {
        const QChar replacement = simplifiedVariantFor(value.at(index));
        if (replacement != value.at(index)) value[index] = replacement;
    }
    return value;
}

